            <br>
            <h3><span class="ml-3">Président du Conseil</span></h3>
            <div class="pt-2 my-3 border">
                <ol> 
                    <li class="pt-1"> 
                        Le mandat du Président du Conseil des Maîtres est de 4 ans.
                    </li>
                    <li class="pt-4"> 
                        Seuls les membres titulaires du Conseil pourront se présenter ou être proposés à la candidature au poste de Président du CDM.
                    </li>
                    <li class="pt-4"> 
                        L’élection du Président du CDM doit être à bulletin secret et à la majorité simple (50%+1) des membres titulaires présents 
                        ou représentés. 
                    </li>
                    <li class="pt-4"> 
                        En cas de vacance forcée du Président du CDM, le Maître le plus âgé et ayant le plus haut grade le remplacera pour procéder 
                        immédiatement à une nouvelle élection dans les 6 mois qui suivent.
                    </li>
                    <li class="pt-4"> 
                        La destitution du Président du CDM peut être provoquée à tout moment avec deux tiers des voix (60%) sur la totalité des membres 
                        titulaires du Conseil.
                    </li>
                    <li class="pt-4"> 
                        Le Président du CDM est un sage, symbole du Vovinam Viet Vo Dao et gardien de la tradition. 
                    </li>
                    <li class="pt-4"> 
                        Le Président du CDM, dans sa plénitude, utilisera sa puissance et ses pouvoirs avec sagesse et modération.
                    </li>
                    <li class="pt-4"> 
                        Le Président du CDM ne peut être candidat au poste de Président Fédéral, ni de membre du Comité Directeur National.
                    </li>
                    <li class="pt-4"> 
                        En cas de vacance forcée du Président Fédéral, c’est le Président du CDM qui le remplacera et procédera immédiatement à des 
                        nouvelles élections dans les 6 mois qui suivent.
                    </li>
                </ol>
            </div>
        </div>
        <div>
            <br>
            <h3><span class="ml-3">Les Membres du Conseil</span></h3>
            <div class="pt-2 my-3 border">
                <ol>
                    <li class="pt-1">
                        Il y a 3 catégories de membre :
                        <ul> 
                            <li class="pt-1">
                                <b> Membre titulaire </b>: membre officiel avec pouvoir de vote. Le nombre de membres titulaires est limité à 15 membres actifs.
                            </li>
                            <li class="pt-4"> 
                                <b> Membre suppléant </b>: membre officiel sans pouvoir de vote. Le nombre de membres suppléants n’est pas limité.
                            </li>
                            <li class="pt-4"> 
                                <b> Membre d’honneur </b>: membre non officiel sans pouvoir de vote. Grâce à son expertise dans un domaine donné, le membre 
                                d’honneur peut être invité par le Président du CDM pour participer aux travaux du Conseil.
                            </li>
                        </ul>
                    </li>
                    <li class="pt-4">
                        Un nouveau membre titulaire doit être élu à l’unanimité par des membres titulaires présents ou représentés.
                    </li>
                    <li class="pt-4">
                        Un nouveau membre suppléant doit être élu à la majorité simple (50%+1) des membres titulaires présents ou représentés.
                    </li>
                    <li class="pt-4">
                        La candidature d’un nouveau membre titulaire ou suppléant n’est valable qu’à la condition suivante : 
                        <ul> 
                            <li class="pt-1"> Titulaire d’un grade de 4ème DANG minimum.</li>
                            <li class="pt-4"> Etre reconnu par le CDM.</li>
                            <li class="pt-4"> Licencié à la Fédération</li>
                            <li class="pt-4"> Etre présenté par au moins 2 membres ou par le Président du CDM.</li>
                        </ul>
                    </li>
                </ol>
            </div>
