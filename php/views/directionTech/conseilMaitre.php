            <h3><span class="ml-3">Qu'est ce que le Conseil des Maîtres ?</span></h3>
            <div class="pt-2 my-3 border">
            <ol>
                <li class="pt-1">
                    Le Conseil National des Maîtres est l’organisation suprême chargée d’organiser, développer et 
                    promouvoir le VOVINAM VIET VO DAO en France.
                </li> 
                <li class="pt-4"> 
                    Le Conseil des Maîtres représente également l’image du Vovinam Viet Vo Dao en assurant l’éthique, 
                    la morale et l’authenticité de la discipline.
                </li>
                <li class="pt-4"> 
                    Le Conseil des Maîtres exerce ses compétences dans le domaine technique et philosophique : 
                    formations, examens des grades, mémoires, thèses, stages, délivrance des diplômes, reconnaissance des grades, 
                    autorisations d’enseignement, nominations des postes techniques comme : 
                    Juges, Arbitres, Directeur Technique National, Entraîneur National, Responsables Techniques Régionaux, Enseignants…
                </li>
            </ol>
